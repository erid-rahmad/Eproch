import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { ICompanyFunctionary } from '@/shared/model/company-functionary.model';

const baseApiUrl = 'api/company-functionaries';

export default class CompanyFunctionaryService {
  public find(id: number): Promise<ICompanyFunctionary> {
    return new Promise<ICompanyFunctionary>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(function(res) {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(function(res) {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ICompanyFunctionary): Promise<ICompanyFunctionary> {
    return new Promise<ICompanyFunctionary>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ICompanyFunctionary): Promise<ICompanyFunctionary> {
    return new Promise<ICompanyFunctionary>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(function(res) {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
